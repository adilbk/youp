			function post_data(__url)
			{
				var id = $('#article_id').val();
				var title = $('#article_title').val();
				var description = $('#article_description').val();
				var content = $('#contenu').html();
				var formData = {id: id, title: title, description: description, content: content}; 
 
				$.ajax({
				    url : __url,
				    type: "POST",
				    data : formData,
				    success: function(data, textStatus, jqXHR)
				    {
				        show_message('success');
				    },
				    error: function (jqXHR, textStatus, errorThrown)
				    {
				 		//alert(errorThrown);
				    }
				});
			}

			function show_message(type)
			{
				$("#success_message").slideDown("slow");
				setTimeout(function(){
				    $("#success_message").slideUp("slow");
				}, 5000);
			}

			function performClick(elemId) 
			{
			   var elem = document.getElementById(elemId);
			   if(elem && document.createEvent) {
			      var evt = document.createEvent("MouseEvents");
			      evt.initEvent("click", true, false);
			      elem.dispatchEvent(evt);
			  	}
				jQuery("#to_upload_image").change(function () {
					if(document.getElementById('to_upload_image').files.length==1)
					{
			    		$('#upload_button').prop('disabled', false);
					}
			    	else
			    		$('#upload_button').prop('disabled', true);
				});
			}

			function updateDivs(__title,__description,__id,__image)
			{
				$('#article_title').val(__title);
				$('#article_title').prop('disabled', false);
				$('#article_description').val(__description);
				$('#article_description').prop('disabled', false);
				$('#slide_to_update').val(__id);
				$('#preview_div').css("background", "url('<?php echo asset_url(); ?>/img/slider_images/"+__image+"')");  
				$('#preview_div').css("background-size","cover");
				$('#preview_div').css("background-repeat","no-repeat");
				$('#preview_div').css("background-position","center center");
				$('#preview_div').css("height","33.333em");
				$('#preview_div').css("overflow","hidden");
				$('#browse_btn').prop('disabled', false);

				$('#article_title').on('input',function(e){
				 		$('#upload_button').prop('disabled', false);
				});
				$('#article_description').on('input',function(e){
				 		$('#upload_button').prop('disabled', false);
				});
			}	

